package org.bibly.logic.exceptions;

public class BibliothecarisException extends Exception
{
    public BibliothecarisException()
    {
        super();
    }

    public BibliothecarisException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public BibliothecarisException(String message)
    {
        super(message);
    }

    public BibliothecarisException(Throwable cause)
    {
        super(cause);
    }
}
